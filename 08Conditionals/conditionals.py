# Conditionals in Python
# if statement
x = 10
if x > 5:
    print("x is greater than 5")

# if-else statement
if x % 2 == 0:
    print("x is even")
else:
    print("x is odd")

# if-elif-else statement
if x < 0:
    print("x is negative")
elif x == 0:
    print("x is zero")
else:
    print("x is positive")

# Nested if statements
if x > 0:
    if x < 20:
        print("x is between 1 and 19")
