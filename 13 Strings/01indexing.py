# Indexing strings in Python
# Syntax: string[index]
# - index: position of the character (0-based index)

a = "hello"
print(a)  # Output: hello
print(a[0])  # Output: h
print(a[1])  # Output: e
print(a[2])  # Output: l
print(a[3])  # Output: l
print(a[4])  # Output: o

print(a[-1])  # Output: o (last character)
print(a[-2])  # Output: l (second to last character)
print(a[-3])  # Output: l (third to last character)
print(a[-4])  # Output: e (fourth to last character)
print(a[-5])  # Output: h (fifth to last character)