set_a = {1, 2, 3, 4}
set_b = {3, 4, 5, 6}

print("Union: ", set_a | set_b)  # Union (All unique elements)
print("Intersection: ", set_a & set_b)  # Intersection (Common elements)
print("Difference: ", set_a - set_b)  # Difference (Elements in set_a but not in set_b)
print("Symmetric Difference: ", set_a ^ set_b)  # Symmetric Difference (Elements in either set but not both)
